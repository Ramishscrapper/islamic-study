// Quran Surah Tab HTML Template
function quranSurahTemplate(surah) {
  return `
      <div class="quran-surah-with-tab">
          <button class="surah surahName">${surah.name}</button>
          <div class="panel">
            <ul>
              <li><span class="surah-details">No. of verses</span>${surah.verses}</li>
              <li><span class="surah-details">No. of rukus</span> ${surah.rukus}</li>
            </ul>
            ${surah.iframe}
          </div>
        </div>
  `;
}

